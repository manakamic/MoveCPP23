// =============================================================================
//  main_value_category.cpp
//  第1章：値カテゴリ（左辺値 lvalue / 右辺値 rvalue）と 右辺値参照 T&&
// -----------------------------------------------------------------------------
//  対応スライド：「1. 値カテゴリ」
//  この章で確認すること：
//    (1) 左辺値は「名前がありアドレスが取れる」／右辺値は「その場限り・名前が無い」
//    (2) 右辺値参照 int&& は右辺値を束縛できる（一時オブジェクトの寿命が延びる）
//    (3) const T& 版 と T&& 版のオーバーロードで、左辺値／右辺値を振り分けられる
//
//  ビルド（VS2026 の「x64 Native Tools Command Prompt」内）：
//    cl /std:c++latest /utf-8 /EHsc main_value_category.cpp
//  ※ 日本語出力を正しく表示するため /utf-8 を必ず付けてください。
// =============================================================================

#include <print>     // C++23: std::print / std::println（iostream は使わない）
#include <utility>   // std::move

// -----------------------------------------------------------------------------
// アドレス表示ヘルパ
//   生ポインタ（int* など）は std::print の "{}" で直接は出せない。
//   （文字列と紛らわしいため）→ const void* にキャストして 16進アドレスにする。
// -----------------------------------------------------------------------------
template <class T>
const void* addr(const T& v) noexcept {
    return static_cast<const void*>(&v);
}

// -----------------------------------------------------------------------------
// 同じ名前で 2 つのオーバーロード。渡された式が
//   左辺値 → const int&（コピー側の役）／右辺値 → int&&（ムーブ側の役）
// を選ぶ。ムーブ／コピーの「振り分け」はこの仕組みが土台。
// -----------------------------------------------------------------------------
void probe(const int& x) {
    std::println("  probe(const int&) が選ばれた … 左辺値(lvalue)  &x = {}", addr(x));
}
void probe(int&& x) {
    std::println("  probe(int&&)      が選ばれた … 右辺値(rvalue)  &x = {}", addr(x));
}

int main() {
    std::println("=== 第1章 値カテゴリ（lvalue / rvalue）===\n");

    int a = 10;      // a は左辺値、10 は右辺値
    int b = a + 5;   // b は左辺値、a + 5 は右辺値（その場限りの計算結果）

    // (1) 左辺値はアドレスが取れる ------------------------------------------------
    std::println("[1] 左辺値は名前があり、アドレスが取れる");
    std::println("    &a = {}", addr(a));
    std::println("    &b = {}", addr(b));
    // &(a + 5);  // ← コンパイルエラー：右辺値のアドレスは取れない（試しに外してみよう）
    std::println("    （a + 5 のような右辺値には & を付けられない）\n");

    // (2) 右辺値参照は右辺値を束縛できる ----------------------------------------
    std::println("[2] 右辺値参照 int&& は右辺値を束縛し、寿命を延ばす");
    int&& r = a + 5;             // 一時オブジェクトの寿命が r まで延びる
    std::println("    int&& r = a + 5;  →  r = {}  /  &r = {}", r, addr(r));
    std::println("    ※ r 自身は「名前がある」ので左辺値。だから &r は取れる\n");

    // (3) オーバーロードでの振り分け --------------------------------------------
    std::println("[3] const T& 版 と T&& 版で、左辺値／右辺値を振り分ける");
    std::println("  ・左辺値 a を渡す:");
    probe(a);                    // → const int&
    std::println("  ・右辺値 10 を渡す:");
    probe(10);                   // → int&&
    std::println("  ・a + 5（右辺値）を渡す:");
    probe(a + 5);                // → int&&
    std::println("  ・std::move(a) を渡す（a を右辺値扱いに“キャスト”）:");
    probe(std::move(a));         // → int&&（a はまだ生きているが、右辺値として扱われる）

    std::println("\n>>> まとめ：名前があれば左辺値、std::move を通すと右辺値扱いになる。");
}
